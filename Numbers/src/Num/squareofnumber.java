package Num;

public class squareofnumber {
       public static void main(String[] args) {
		
    	   int n=25;
    	   int res=0;
    	   while(n>10)
    	   {
    		   res=n/2;
    		   res=res/2;
    		   n=n/10;
    	   }
    	   System.out.println(res);
    	   
    	   
	}
}
